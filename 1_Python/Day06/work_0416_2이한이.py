# [문제 7]
# 어떤 수 n이 10보다 크면 n을 그대로, 10 이하이면 n * 2를 반환하는
# 조건부 표현식을 작성하세요.
 
n= int(input("정수 n을 입력하시오: "))
result= n if n> 10 else n* 2
print(result)


