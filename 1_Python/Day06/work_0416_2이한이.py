# [문제 5]
# 문자열 name이 빈 문자열("")이면 "이름 없음", 아니면 name 값을 그대로
# 반환하는 조건부 표현식을 작성하세요.

name = input("이름을 입력하시오: ")
result= "이름 없음" if name == "" else name
print(result)

