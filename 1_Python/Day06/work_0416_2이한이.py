# [문제 1]
# 정수 n을 입력받아, n이 양수이면 "양수", 그렇지 않으면 "양수 아님"을 출력하세요.
# 조건부 표현식 한 줄로 작성하세요.

n = int(input("정수 n 을 입력하시오: "))
result = "양수" if n > 0 else "양수 아님"
print(result)

# ----------------------------------------------------------------

# [문제 2]
# 정수 n이 짝수이면 "even", 홀수이면 "odd"를 반환하는
# 조건부 표현식을 작성하세요.


n = int(input("정수 n 을 입력하시오: "))
result = "even" if n % 2 == 0 else "odd"
print(result)


# ----------------------------------------------------------------


# [문제 3]
# 두 수 a, b 중 더 큰 수를 반환하는 조건부 표현식을 작성하세요.
# (내장 함수 max() 사용 금지)

a= int(input("정수 n 을 입력하시오: "))
b= int(input("정수 n 을 입력하시오: "))
result = a if a > b else b
print(result)

