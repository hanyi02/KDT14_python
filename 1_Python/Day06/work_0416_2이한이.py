# [문제 9]
# 리스트 items가 비어 있으면 "목록이 비었습니다",
# 비어 있지 않으면 items의 첫 번째 원소를 반환하는
# 조건부 표현식을 작성하세요.

items= input("리스트 원소들을 공백으로 구분해서 입력하시오(비우면 빈 리스트): ").split()
result= items[0] if items else "목록이 비었습니다"
print(result)


