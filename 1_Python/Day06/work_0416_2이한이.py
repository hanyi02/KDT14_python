# [문제 10]
# 두 문자열 s1, s2를 비교하여 같으면 "동일", 다르면 "다름"을 반환하는
# 조건부 표현식을 작성하세요.

s1 = input("첫 번째 문자열을 입력하시오: ")
s2 = input("두 번째 문자열을 입력하시오: ")
result = "동일" if s1 == s2 else "다름"
print(result)


