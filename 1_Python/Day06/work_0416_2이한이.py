# [문제 4]
# 변수 is_raining이 True이면 "우산을 챙기세요", False이면 "우산 불필요"를
# 출력하는 조건부 표현식을 작성하세요.

is_raining= bool(int(input("비 오면 1, 안 오면 0")))
result = "우산을 챙기세요" if is_raining else "우산 불필요"
print(result)

