print('#----------------------------------')

files = ['intra.h', 'intra.c', 'define.h', 'run.py', 'ex01.py', 'intro.hwp']

# (1) 확장자 없이 파일명만 출력
print('[1]---')
for j in files:
    print(j.split('.')[0])

# (2) 확장자가 ‘h’이거나 ‘c’인 것만 출력
print('\n[2]---')
for j in files:
    확장자= j.split('.')[1]
    if 확장자== 'h' or 확장자== 'c':
        print(확장자)

# (3) 파일명에 ‘e’가 2개 이상 있고 ‘f’가 있는 파일명만 출력
print('\n[3]---')
for j in files:
    name = j.split('.')[0]
    if name.count('e') >= 2 and 'f' in name:
        print(j)


