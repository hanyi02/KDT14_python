## =================================================================

# 키 존재 여부

print(f" '경제' in jumsuDict1: {'경제' in jumsuDict1}")
print(f" '경제' in jumsuDict2: {'경제' in jumsuDict2}")

# 값 존재 여부==> 에러
print(f" 99 in jumsuDict1: {99 in jumsuDict1}")
print(f" 99 in jumsuDict2: {99 in jumsuDict2}")



