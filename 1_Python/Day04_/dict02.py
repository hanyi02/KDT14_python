## dick 데이터 생성
## -------------------------
## [예시] 6개 과목별 점수 저장
#   국어 99, 미술 80, 음악 100, 물리 90, 수학 81, 영어 72
jumsuDict1={'국어': 99, '미술': 80, '음악': 100, '물리': 90, '수학':81, '영어': 72 }
jumsuDict2={'국어': 99, '체육': 80, '경제': 100}

print(f"jumsuDict1=> 타입:{type(jumsuDict1)}, 원수 개수:{len(jumsuDict1)}, 데이터: {jumsuDict1} ")
print(f"jumsuDict=> 타입:{type(jumsuDict2)}, 원수 개수:{len(jumsuDict2)}, 데이터: {jumsuDict2} ")




