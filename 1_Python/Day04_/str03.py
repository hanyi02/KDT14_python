## [11] 원하는 출력형태 설정을 위한 메서드: format()
##      형식: "{} {}".format({}에 들어갈 데이터를 설정)
##      방식:  (1) index 방식: "{0} {1}".format(0번 인덱스 데이터, 1번 인덱스 데이터)
##            (2) name 방식: "{age} {birth}". format(age= 데이터, birth= 데이터) 
## =======================================
## [예] 출력형태: 2026년도 대학수학능력시험일자: 11월 12일 목요일
d_day="{0}년도 대학수학능력시험일자: 11월 {1}일 목요일".format('2026', '12')
print(f"d_day: {d_day}")

# {} 개수보다 적으면 에러
# d_day="{0}년도 대학수학능력시험일자: 11월 {1}일 목요일".format('2026')
print(f"d_day: {d_day}")

# {} 개수보다 많으면 무시
d_day="{0}년도 대학수학능력시험일자: 11월 {1}일 목요일".format('2026', '12', '--')
print(f"d_day: {d_day}")

# {} 와 동일 개수면 빈칸도 가능
d_day="{}년도 대학수학능력시험일자: 11월 {}일 목요일".format('2026', '12')
print(f"d_day: {d_day}")

# 방식 2
# 문자
d_day="{year}년도 대학수학능력시험일자: 11월 {day}일 목요일".format(year='2026', day='12')
print(f"d_day: {d_day}")

# 정수
d_day="{year}년도 대학수학능력시험일자: 11월 {day}일 목요일".format(year=2026, day=12)
print(f"d_day: {d_day}")




