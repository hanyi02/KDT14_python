## { } 중괄호 안에 데이터 출력에 대한 설정
##      형식: {인덱스 또는 이름: 설정부분}
##      방식:  => 사용하는 칸수 설정 {인덱스 또는 이름: 칸수} {0:10} : 인덱스 0에서 10칸
##            => 데이터 정렬 방식 설정 {인덱스 또는 이름: 정렬방식칸수}=>  가운데 정렬{0:^10}, 왼쪽 정렬 {0:<10}, 오른쪽 정렬 {0:>10} 
##            => 공백처리 설정 {인덱스 또는 이름: 공백설정정렬방식칸수}=> 가운데 정렬 공백 10설정 {0:*^10}
##            => 실수 자릿수 설정: {인덱스 또는 이름: 공백설정정렬방식칸수.실수자릿수f} {0:*^10.3f}
##
## ★★★ 설정한 자릿수/ 칸수보다 큰 데이터 경우?? 데이터 손실이 되면 안 됨! 설정 무시
## =======================================

msg= "Good Luck"

print("[기본] 오늘의 남긴 말:{}". format(msg))
print("[설정] 오늘의 남긴 말:{:>14}". format(msg))
print("[설정] 오늘의 남긴 말:{:<14}". format(msg))
print("[설정] 오늘의 남긴 말:{:^14}". format(msg))
print("[설정] 오늘의 남긴 말:{:*^14}". format(msg))

