import pandas as pd

# 파일 읽기
df = pd.read_json("../Data/students_records.json")

print("원본 데이터")
print(df)

# =================================================
# 문제 1. score가 90점 이상인 학생만 출력
# =================================================
print("\n문제 1")
print(df[df["score"] >= 90])

# =================================================
# 문제 2. age가 21세 이상인 학생만 출력
# =================================================
print("\n문제 2")
print(df[df["age"] >= 21])

# =================================================
# 문제 3. gender가 '여'인 학생만 출력
# =================================================
print("\n문제 3")
print(df[df["gender"] == "여"])

# =================================================
# 문제 4. major가 '데이터분석'인 학생만 출력
# =================================================
print("\n문제 4")
print(df[df["major"] == "데이터분석"])

# =================================================
# 문제 5. score가 80점 이상이고 gender가 '여'인 학생만 출력
# =================================================
print("\n문제 5")
print(df[(df["score"] >= 80) & (df["gender"] == "여")])

# =================================================
# 문제 6. age가 21세 이상이고 score가 90점 이상인 학생의 name, age, score 컬럼만 출력
# =================================================
print("\n문제 6")
print(df.loc[(df["age"] >= 21) & (df["score"] >= 90), ["name", "age", "score"]])

# =================================================
# 문제 7. isin() 사용: major가 데이터분석 또는 인공지능인 학생만 출력
# =================================================
print("\n문제 7")
print(df[df["major"].isin(["데이터분석", "인공지능"])])

# =================================================
# 문제 8. isin() 사용: name이 김민수, 최유진, 정다은 중 하나인 학생만 출력
# =================================================
print("\n문제 8")
print(df[df["name"].isin(["김민수", "최유진", "정다은"])])

# =================================================
# 문제 9. query() 사용: score가 90점 이상인 학생만 출력
# =================================================
print("\n문제 9")
print(df.query("score >= 90"))

# =================================================
# 문제 10. query() 사용: age가 21세 이상이고 score가 85점 이상인 학생만 출력
# =================================================
print("\n문제 10")
print(df.query("age >= 21 and score >= 85"))

# =================================================
# 문제 11. 각 컬럼의 자료형 확인
# =================================================
print("\n문제 11")
print(df.dtypes)

# =================================================
# 문제 12. id 컬럼을 문자열형(str)으로 변환 후 자료형 확인
# =================================================
print("\n문제 12")
df["id"] = df["id"].astype(str)
print(df.dtypes)

# =================================================
# 문제 13. age 컬럼을 int8로 변환 후 자료형 확인
# =================================================
print("\n문제 13")
df["age"] = df["age"].astype("int8")
print(df.dtypes)

# =================================================
# 문제 14. score 컬럼을 float으로 변환 후 자료형 확인
# =================================================
print("\n문제 14")
df["score"] = df["score"].astype(float)
print(df.dtypes)

# =================================================
# 문제 15. score 컬럼을 astype(str)으로 문자열형 변환 후 자료형 확인
# =================================================
print("\n문제 15")
df["score"] = df["score"].astype(str)
print(df.dtypes)

# =================================================
# 문제 16. 문자열형 score 컬럼을 pd.to_numeric()으로 다시 숫자형 변환
# =================================================
print("\n문제 16")
df["score"] = pd.to_numeric(df["score"])
print(df.dtypes)

# =================================================
# 문제 17. age 컬럼을 문자열형으로 변환 후 pd.to_numeric()으로 다시 숫자형 변환
# =================================================
print("\n문제 17")
df["age"] = df["age"].astype(str)
df["age"] = pd.to_numeric(df["age"])
print(df.dtypes)

# =================================================
# 문제 18. 잘못된 문자열 값 추가 후 errors='coerce'로 NaN 처리
# =================================================
print("\n문제 18")
df.loc[5] = [6, "테스트", "남", 24, "컴퓨터공학", "점수없음", "2026-03-10"]
df["score"] = pd.to_numeric(df["score"], errors="coerce")
print(df)
print(df.dtypes)

# =================================================
# 문제 19. join_date 컬럼을 날짜형으로 변환 후 자료형 확인
# =================================================
print("\n문제 19")
df["join_date"] = pd.to_datetime(df["join_date"])
print(df.dtypes)

# =================================================
# 문제 20. join_date 날짜형 변환 후 가입일이 2026-03-05 이후인 학생만 출력
# =================================================
print("\n문제 20")
df["join_date"] = pd.to_datetime(df["join_date"])
print(df[df["join_date"] >= "2026-03-05"])
