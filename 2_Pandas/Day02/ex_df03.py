## ===========================================
##          DataFrame 행과 열 다루기 
## -------------------------------------------
## * 행 선택
## => df변수명.iloc[정수 위치인덱스]
## => df변수명.loc[라벨 인덱스]
## * 열 선택
## => df변수명.[컬럼이름]
## => df변수명.컬럼이름 : 단 이름이 str인 경우
##  
## * 함수 : DataFrame(datas) 
## ===========================================
## -------------------------------------------
## [1] 모듈 로딩
## -------------------------------------------
import pandas as pd 

## -------------------------------------------
## [2] DataFrame 인스턴스 생성
## -------------------------------------------
## 데이터 준비
datas = {'A' : [10, 20],
         'B' : [33, 55],
         'C' : [9, 8] }

## DataFrame 인스턴스 생성
df = pd.DataFrame(datas)

## -------------------------------------------
## [3] DataFrame 인스턴스 속성
## -------------------------------------------
print('- DataFrame 기본 속성들 -')
print(f'* index   : {df.index}')
print(f'* columns : {df.columns}')

## -------------------------------------------
## [4] 열/ 컬럼 선택
## -------------------------------------------
print("\n ---- 3번 컬럼 선택 ---- ")
print(df[3])
 


## -------------------------------------------
## [5] 행/ 로우 선택
## -------------------------------------------
print("\n ---- 3번 행 선택 ---- ")
print(df.iloc[3])